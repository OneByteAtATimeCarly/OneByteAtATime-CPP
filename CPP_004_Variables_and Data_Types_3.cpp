//---------------------------------------------------------------------------------------------------------------------------

#include <iostream.h>

//---------------------------------------------------------------------------------------------------------------------------

typedef unsigned short int USHORT; //typedef defined
typedef unsigned long ULONG;

//---------------------------------------------------------------------------------------------------------------------------

int main()
{
    USHORT Width = 5;
    USHORT Length;
    Length = 10;

    ULONG Area = Width * Length;

    cout << "Width:" << Width << "\n";
    cout << "Length: " << Length << endl;
    cout << "Area: " << Area <<endl;

    return 0;
}

