#include <iostream.h>

//---------------------------------------------------------------------------------------------------------------------------

int main()
{
    //A Parallel Array
     char People[5][20] = { "Harry", "Susie", "Sally", "Charles", "Drew" };
     int ages[5] = { 15, 24, 13, 34, 35 };

     for(int x = 0; x < 5; x++) {

         cout << People[x] << " is " << ages[x] << endl;

     }
}

